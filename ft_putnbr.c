#include "so_long.h"

void	ft_putnbr(int n)
{
	char	a;

	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
	}
	else
	{
		if (n < 0)
		{
			write(1, "-", 1);
			n = (n * (-1));
		}
		if (n < 10)
		{
			a = n + '0';
			write(1, &a, 1);
		}
		else
		{
			ft_putnbr(n / 10);
			a = (n % 10) + '0';
			write(1, &a, 1);
		}
	}
}
